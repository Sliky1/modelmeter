/**
 * 返回当前 VS Code 界面语言对应的翻译表。
 * 匹配优先级：精确匹配 (zh-cn) → 语言前缀匹配 (zh) → 英文兜底
 */
export function t(): I18nStrings {
    const lang = vscode.env.language.toLowerCase();
    return (
        translations[lang] ??
        translations[lang.split('-')[0]] ??
        translations['en']
    );
}

/**
 * 字符串格式化：将 {0} {1} ... 替换为对应参数
 * @example fmt(t().confirmDeleteMsg, alias)
 */
export function fmt(template: string, ...args: string[]): string {
    return template.replace(/\{(\d+)\}/g, (_, i) => args[Number(i)] ?? '');
}
